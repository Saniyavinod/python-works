# arithmetic operators
num1=100
num2=200


#addition
add=num1+num2

print(f"{num1}+{num2}={add}")


#subtraction
sub=num1-num2

print(f"{num1}-{num2}={sub}")


#multiplication
mul=num1*num2

print(f"{num1}*{num2}={mul}")


#division
div=num1/num2

print(f"{num1}/{num2}={div}")

#modulus
mod=num1%num2

print(f"{num1}%{num2}={mod}")


#exponent
exp=num1**2

print(f"{num1}**{2}={exp}")


#floor division
res=num1//num2
print(res)
